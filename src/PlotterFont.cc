// DIN differs from the international plotter font only in glyph '0': the
// diagonal slash is replaced by a central dot. This keeps the ROM outline and
// draws a small square in the center.
constexpr std::array<uint8_t, 16> prnC41DINZeroGlyph = {{
	0x01, 0x0b, 0x1a, 0x09, 0x38, 0x0f, 0x1e, 0x0d,
	0x3c, 0x11, 0x08, 0x0a, 0x0c, 0x0e, 0x03, 0xb4,
}};

[[nodiscard]] constexpr gl::vec2 directionDelta(uint8_t direction)
{
	// The ROM X axis is mirrored compared to manual-oriented rendering.
	switch (direction) {
	case 0: return { 0.0f,  1.0f};
	case 1: return { 1.0f,  1.0f};
	case 2: return { 1.0f,  0.0f};
	case 3: return { 1.0f, -1.0f};
	case 4: return { 0.0f, -1.0f};
	case 5: return {-1.0f, -1.0f};
	case 6: return {-1.0f,  0.0f};
	case 7: return {-1.0f,  1.0f};
	default: UNREACHABLE;
	}
}

