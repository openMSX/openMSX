std::span<const uint8_t> getPRNC41Glyph(PlotterFontVariant variant, uint8_t code)
{
	if (variant == PlotterFontVariant::DIN && code == '0') {
		return prnC41DINZeroGlyph;
	}

	auto start = prnC41GlyphStarts[code];
	auto size = prnC41GlyphStarts[size_t(code) + 1] - start;
	return std::span{prnC41GlyphData}.subspan(start, size);
}

std::vector<PlotterFontCommand> decodePRNC41Glyph(
	PlotterFontVariant variant, uint8_t code)
{
	auto glyph = getPRNC41Glyph(variant, code);
	std::vector<PlotterFontCommand> result;
	result.reserve(glyph.size());

	for (uint8_t raw : glyph) {
		auto command = raw & 0x7f;
		auto length = float(((command >> 4) & 0x07) + 1);
		auto nibble = command & 0x0f;
		auto type = (nibble & 0x08) ? PlotterFontCommandType::Draw
		                            : PlotterFontCommandType::Move;
		auto direction = uint8_t(nibble & 0x07);
		result.push_back({type, length * directionDelta(direction), raw});

		if (raw & 0x80) break;
	}
	return result;
}

